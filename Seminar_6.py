# Дан массив, состоящий из целых чисел. Напишите программу,
# которая в данном массиве определит количество элементов,
# у которых два соседних и, при этом, оба соседних элемента меньше данного.
# Сначала вводится число N — количество элементов в массиве
# Далее записаны N чисел — элементы массива. Массив состоит из целых чисел.

my_list = [random.randint(1, 10) for _ in range(int(input('Введите размер массива: ')))]
print(my_list)

count = 1 if my_list[-2] < my_list[-1] > my_list[0] else 0
for i in range(0, len(my_list)-1):
    if my_list[i-1] < my_list[i] > my_list[i+1]:
        count += 1
print(count)

