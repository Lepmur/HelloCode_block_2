# Даны два массива чисел. Требуется вывести те элементы первого массива
# (в том порядке, в каком они идут в первом массиве), которых нет во втором массиве.
# Пользователь вводит число N - количество элементов в первом массиве,
# затем N чисел - элементы массива. Затем число M - количество элементов
# во втором массиве. Затем элементы второго массива
import random

first_list = [random.randint(1, 10) for _ in range(int(input('Введите размер массива: ')))]
print(first_list)
second_list = [random.randint(1, 10) for _ in range(int(input('Введите размер массива: ')))]
print(second_list)

new_list = []
for item in first_list:
    if item not in second_list:
        new_list.append(item)
print(new_list)

